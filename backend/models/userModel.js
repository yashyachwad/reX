const {Schema,model} = require("mongoose");

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },

   email: {
    type: String,
    required: true,
    unique:true 
  },

   password: {
    type: String,
    required: true,
  },

},

 {
    timestamps:true
 }

);

const UserModel = model("User", UserSchema)         // created model of above scheman 

module.exports = UserModel